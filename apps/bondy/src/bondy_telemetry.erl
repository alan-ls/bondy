-module(bondy_telemetry).

-export([trace_id/0]).




%% =============================================================================
%% API
%% =============================================================================



%% -----------------------------------------------------------------------------
%% @doc Generates a 128 bit random integer to use as a trace id.
%% @end
%% -----------------------------------------------------------------------------
-spec trace_id() -> integer().

trace_id() ->
    %% 2 shifted left by 127 == 2 ^ 128
    rand:uniform(2 bsl 127 - 1).